print("Programme qui calcule le périmetre d'un rectangle")
print("______________________________")

largeur = float(input("Indiquez la largeur du rectangle : "))
longueur = float(input("Indiquez la longueur du rectangle : "))

perimetre = (longueur + largeur) * 2

print("Le périmetre du rectangle est de", perimetre, "unités")